package com.house.bed;

public class Bed {

    private String mattress;
    private String mattressSize;
    private String headBoard;

    public String getMattress() {
        return mattress;
    }

    public void setMattress(String mattress) {
        this.mattress = mattress;
    }

    public String getMattressSize() {
        return mattressSize;
    }

    public void setMattressSize(String mattressSize) {
        this.mattressSize = mattressSize;
    }

    public String getHeadBoard() {
        return headBoard;
    }

    public void setHeadBoard(String headBoard) {
        this.headBoard = headBoard;
    }
}
